    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Produtos obj = new Produtos();
        obj.setDescricao(txtDescricao.getText());
        obj.setPreco(Double.parseDouble(txtQtdNova.getText()));
        obj.setQtd_estoque(Integer.parseInt(txtQtdAtual.getText()));

        
        ProdutosDao dao = new ProdutosDao();
        dao.salvar(obj);
        Utilitarios util = new Utilitarios();
        util.limpaTela(painel_estoque);
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String nome = txtDescricao.getText();
        Produtos obj = new Produtos();
        ProdutosDao dao = new ProdutosDao();
        Fornecedores f = new Fornecedores();
        FornecedoresDao daof = new FornecedoresDao();
               
        
        obj = dao.buscarProduto(nome);
        if (obj.getDescricao()!= null){
            txtCodigo.setText(String.valueOf(obj.getId()));
            txtDescricao.setText(obj.getDescricao());
            txtQtdNova.setText(String.valueOf(obj.getPreco()));
            txtQtdAtual.setText(String.valueOf(obj.getQtd_estoque()));
            
            f = daof.buscarFornecedor(obj.getFornecedores().getNome());

        } else {
            JOptionPane.showMessageDialog(null, "Produto não encontrado");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

